# ---------- Helpers ----------
def mask_key(key: str, tail: int = 4) -> str:
    if not key:
        return "(none)"
    if len(key) <= tail:
        return "*" * len(key)
    return "*" * (len(key) - tail) + key[-tail:]

def read_file(path: Path) -> str:
    if path.exists():
        return path.read_text(encoding="utf-8").strip()
    return ""

def save_file(path: Path, value: str):
    path.write_text(value.strip() + "\n", encoding="utf-8")
    if os.name != "nt":
        try:
            path.chmod(0o600)
        except Exception:
            pass

def prompt_api_key() -> str:
    print("Enter your OpenRouter API key (input hidden). Leave blank to cancel.")
    key = getpass.getpass("> ").strip()
    return key

def prompt_domain() -> str:
    dom = input("Enter the target domain (e.g. example.com). Leave blank to cancel:\n> ").strip()
    return dom

def ensure_api_and_domain():
    """Load existing or prompt if missing."""
    api = read_file(API_FILE)
    dom = read_file(DOMAIN_FILE)

    if not api:
        print("No API key found.")
        k = prompt_api_key()
        if k:
            save_file(API_FILE, k)
            api = k
            print("API key saved.")
        else:
            print("API key not set. Some operations may fail.")
    if not dom:
        print("No domain found.")
        d = prompt_domain()
        if d:
            save_file(DOMAIN_FILE, d)
            dom = d
            print("Domain saved.")
        else:
            print("Domain not set. Some operations may fail.")
    return api, dom

def run_application(api_key: str, domain: str):
    if not Path(APP_SCRIPT).exists():
        print(f"Error: {APP_SCRIPT} not found in current directory.")
        return
    env = os.environ.copy()
    if api_key:
        env["OPENROUTER_API_KEY"] = api_key
    if domain:
        env["TARGET_DOMAIN"] = domain
    cmd = [sys.executable, APP_SCRIPT]
    if domain:
        cmd.append(domain)
    print(f"\nLaunching {APP_SCRIPT} (press Ctrl+C to stop it)...\n")
    try:
        # This blocks until the script finishes; returns control to menu afterwards
        subprocess.run(cmd, env=env)
    except KeyboardInterrupt:
        print("\nInterrupted: returning to menu.")
    except Exception as e:
        print("Failed to run application:", e)

def show_status(api_key: str, domain: str):
    print("\nCurrent configuration:")
    print("  API key:   ", mask_key(api_key))
    print("  Domain:    ", domain if domain else "(none)")
    print("  App script:", APP_SCRIPT)
    print()

# ---------- Menu ----------
def menu_loop():
    # Load or prompt initially
    api_key, domain = ensure_api_and_domain()

    def handle_sigint(sig, frame):
        print("\nCaught Ctrl+C — exiting.")
        sys.exit(0)

    signal.signal(signal.SIGINT, handle_sigint)

    while True:
        print("Menu:")
        print("  1) Run application")
        print("  2) Change / Set API key")
        print("  3) Change / Set domain")
        print("  4) Show current values")
        print("  5) Exit")
        choice = input("\nSelect an option [1-5]: ").strip()

        if choice == "1":
            # refresh values from files in case user edited externally
            api_key = read_file(API_FILE) or api_key
            domain = read_file(DOMAIN_FILE) or domain
            run_application(api_key, domain)

        elif choice == "2":
            k = prompt_api_key()
            if not k:
                print("No input; API key unchanged.")
            else:
                save_file(API_FILE, k)
                api_key = k
                print("API key saved.")

        elif choice == "3":
            d = prompt_domain()
            if not d:
                print("No input; domain unchanged.")
            else:
                save_file(DOMAIN_FILE, d)
                domain = d
                print("Domain saved.")

        elif choice == "4":
            show_status(api_key, domain)

        elif choice == "5":
            print("Exiting. Goodbye!")
            sys.exit(0)

        else:
            print("Invalid choice — pick 1,2,3,4 or 5.")

# ---------- Entry ----------
if __name__ == "__main__":
    try:
        menu_loop()
    except KeyboardInterrupt:
        print("\nExiting (Ctrl+C). Goodbye!")
        sys.exit(0)
